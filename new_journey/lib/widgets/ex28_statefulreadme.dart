// Stateful widget lifecycle
/*


 1. createState() ➡️ Creates the State Object.  [Once]
 2. iniState() ➡️ Initialize the state and resources. [Once]
 3. didChangeDependencies() ➡️ Called when dependencies change.
 4. build() ➡️ Called to build the widget's UI.

 5. setState() ➡️ Triggered to update the state and rebuild.
 6. didUpdateWidget() ➡️ Called when widget configuration changes.
 7. deactivate() ➡️ Called when the widget is temporarily removed.
 8. dispose() ➡️ Called when the widget is permanently removed.
 
 */
